#' Quadratic Assignment Procedure correlation test
#'
#' Run a QAP correlation test between two networks or network-like matrices.
#'
#' This function provides a \pkg{snafun}-style interface to the classic QAP
#' correlation test. The two focal inputs, \code{x} and \code{y}, can each be
#' supplied as an \code{igraph} object, a \code{network} object, a square
#' adjacency matrix, or an edgelist \code{data.frame}. Optional control
#' matrices can be added to obtain a partial correlation.
#'
#' Internally, all supported inputs are converted to numeric one-mode matrices
#' of identical size. The observed statistic is then just the ordinary
#' correlation between the vectorized adjacency matrices after applying the
#' requested directed/undirected and diagonal conventions. For directed
#' analyses, all dyads are used, optionally including the diagonal. For
#' undirected analyses, the full symmetric adjacency matrix is vectorized as-is;
#' this keeps the interpretation close to the matrix representation students
#' actually work with when they call \code{cor(c(A), c(B))}. QAP replications
#' are generated by simultaneously permuting the rows and columns of \code{y}
#' while keeping \code{x} and any control matrices fixed. This matches the usual
#' interpretation of QAP as testing whether \code{y} is associated with
#' \code{x} beyond what would be expected under vertex relabeling.
#'
#' When \code{controls} are supplied, \code{stat_qap_cor()} reports a partial
#' correlation. Concretely, the vectorized values of \code{x} and \code{y} are
#' each residualized on the control matrices, after which the correlation
#' between the two residual vectors is computed. With \code{method = "spearman"}
#' the vectors are first converted to ranks and the residual correlation is then
#' calculated on those ranks.
#'
#' For directed networks, this observed statistic coincides with
#' \code{\link[sna]{gcor}} once the same diagonal choice is made. For undirected
#' networks without diagonal entries, it also coincides with
#' \code{\link[sna]{gcor}} because duplicate off-diagonal cells cancel out in the
#' correlation. When \code{diagonal = TRUE} for an undirected network,
#' \code{stat_qap_cor()} follows the full adjacency-matrix representation rather
#' than the triangle-only convention used by \pkg{sna}. The permutation test
#' itself is implemented directly in \pkg{snafun}, so it works naturally with
#' the graph classes used throughout the package.
#'
#' @param x First network or network-like object.
#' @param y Second network or network-like object.
#' @param controls Optional control matrix or list of control matrices. Each
#'   control must be supplied in one of the same formats as \code{x} and
#'   \code{y}.
#' @param method Correlation method. One of \code{"pearson"} or
#'   \code{"spearman"}.
#' @param reps Number of QAP permutations.
#' @param diagonal Logical scalar; should diagonal entries be included?
#' @param directed How should the input be interpreted? One of \code{"auto"},
#'   \code{"directed"}, or \code{"undirected"}. The default infers this from
#'   \code{x}.
#' @param alternative Alternative hypothesis. One of \code{"two.sided"},
#'   \code{"greater"}, or \code{"less"}.
#' @param seed Optional integer seed for reproducible permutations.
#'
#' @return An object of class \code{stat_qap_cor} containing the observed
#'   correlation, the permutation distribution, and empirical p-values. The
#'   returned object stores the proportions of replicated statistics that are
#'   strictly greater than, strictly less than, and numerically equal to the
#'   observed value as \code{p.greater}, \code{p.less}, and \code{p.equal}.
#' @family statistics functions
#' @export
#'
#' @examples
#' x <- snafun::create_manual_graph(A -- B, B -- C, C -- D)
#' y <- snafun::create_manual_graph(A -- B, B -- D, C -- D)
#'
#' stat_qap_cor(x, y, reps = 99)
#'
#' x_n <- snafun::to_network(x)
#' y_m <- snafun::to_matrix(y)
#' stat_qap_cor(x_n, y_m, reps = 49, directed = "undirected")
#'
#' z <- snafun::add_vertices(
#'   snafun::create_manual_graph(A -- C, B -- C),
#'   vertices = "D"
#' )
#' stat_qap_cor(x, y, controls = z, reps = 49)
#'
#' z2 <- snafun::add_vertices(
#'   snafun::create_manual_graph(A -- D, B -- D),
#'   vertices = "C"
#' )
#' stat_qap_cor(x, y, controls = list(z, z2), reps = 49)
#'
#' xm <- snafun::to_matrix(x)
#' ym <- snafun::to_matrix(y)
#' zm <- snafun::to_matrix(z)
#' stat_qap_cor(xm, ym, controls = zm, reps = 49, diagonal = TRUE)
stat_qap_cor <- function(x,
                         y,
                         controls = NULL,
                         method = c("pearson", "spearman"),
                         reps = 1000,
                         diagonal = FALSE,
                         directed = c("auto", "directed", "undirected"),
                         alternative = c("two.sided", "greater", "less"),
                         seed = NULL) {
  method <- method[[1]]
  directed <- directed[[1]]
  alternative <- alternative[[1]]

  if (!method %in% c("pearson", "spearman")) {
    stop("'method' should be either 'pearson' or 'spearman'")
  }
  if (!directed %in% c("auto", "directed", "undirected")) {
    stop("'directed' should be one of 'auto', 'directed', or 'undirected'")
  }
  if (!alternative %in% c("two.sided", "greater", "less")) {
    stop("'alternative' should be one of 'two.sided', 'greater', or 'less'")
  }
  if (!is.logical(diagonal) || length(diagonal) != 1L || is.na(diagonal)) {
    stop("'diagonal' should be either TRUE or FALSE")
  }
  if (length(reps) != 1L || is.na(reps) || reps < 1 || reps != as.integer(reps)) {
    stop("'reps' should be a single positive integer")
  }
  reps <- as.integer(reps)
  if (!is.null(seed) && (length(seed) != 1L || is.na(seed))) {
    stop("'seed' should be NULL or a single non-missing integer value")
  }

  seed_state <- stat_qap_cor_set_seed(seed = seed)
  if (is.function(seed_state$restore)) {
    on.exit(seed_state$restore(), add = TRUE)
  }

  x_matrix_raw <- stat_qap_cor_prepare_matrix(x = x, directed_override = NULL)
  directed_flag <- stat_qap_cor_resolve_direction(
    x = x_matrix_raw,
    directed = directed
  )
  x_matrix <- stat_qap_cor_prepare_matrix(x = x, directed_override = directed_flag)
  y_matrix <- stat_qap_cor_prepare_matrix(
    x = y,
    directed_override = directed_flag,
    reference = x_matrix
  )
  control_matrices <- stat_qap_cor_prepare_controls(
    controls = controls,
    directed_flag = directed_flag,
    reference = x_matrix
  )

  obs_stat <- stat_qap_cor_compute(
    x = x_matrix,
    y = y_matrix,
    controls = control_matrices,
    method = method,
    diagonal = diagonal,
    directed = directed_flag
  )

  rep_stats <- numeric(reps)
  for (rep_index in seq_len(reps)) {
    permuted_y <- stat_qap_cor_permute_matrix(y_matrix)
    rep_stats[[rep_index]] <- stat_qap_cor_compute(
      x = x_matrix,
      y = permuted_y,
      controls = control_matrices,
      method = method,
      diagonal = diagonal,
      directed = directed_flag
    )
  }

  valid_replicates <- !(is.na(rep_stats) | is.nan(rep_stats))
  p_values <- stat_qap_cor_empirical_p(
    obs_stat = obs_stat,
    rep_stats = rep_stats,
    alternative = alternative
  )

  out <- list(
    obs.stat = obs_stat,
    rep.stat = rep_stats,
    method = method,
    diagonal = diagonal,
    mode = if (directed_flag) "digraph" else "graph",
    alternative = alternative,
    reps = reps,
    valid.reps = sum(valid_replicates),
    p.value = p_values$p.value,
    p.greater = p_values$p.greater,
    p.less = p_values$p.less,
    p.equal = p_values$p.equal,
    controls.used = length(control_matrices),
    n = nrow(x_matrix),
    seed = seed,
    call = match.call()
  )
  class(out) <- "stat_qap_cor"
  out
}


#' Summarize a QAP correlation result
#'
#' Summarize the permutation distribution from \code{\link{stat_qap_cor}}.
#'
#' @param object Object returned by \code{\link{stat_qap_cor}}.
#' @param na.rm Logical scalar; should missing replicate statistics be removed
#'   before summarizing?
#' @param ... Ignored.
#'
#' @return An object of class \code{summary.stat_qap_cor}.
#' @export
summary.stat_qap_cor <- function(object, na.rm = TRUE, ...) {
  rep_values <- object$rep.stat
  if (isTRUE(na.rm)) {
    rep_values <- rep_values[!(is.na(rep_values) | is.nan(rep_values))]
  }

  null_summary <- if (length(rep_values) == 0L) {
    c(
      Min. = NA_real_,
      `1st Qu.` = NA_real_,
      Median = NA_real_,
      Mean = NA_real_,
      `3rd Qu.` = NA_real_,
      Max. = NA_real_
    )
  } else {
    summary(rep_values)
  }

  null_sd <- if (length(rep_values) <= 1L) {
    NA_real_
  } else {
    stats::sd(rep_values)
  }

  out <- list(
    obs.stat = object$obs.stat,
    rep.stat = object$rep.stat,
    method = object$method,
    diagonal = object$diagonal,
    mode = object$mode,
    alternative = object$alternative,
    reps = object$reps,
    valid.reps = object$valid.reps,
    p.value = object$p.value,
    p.greater = object$p.greater,
    p.less = object$p.less,
    p.equal = object$p.equal,
    controls.used = object$controls.used,
    n = object$n,
    seed = object$seed,
    call = object$call,
    null.summary = null_summary,
    null.sd = null_sd
  )
  class(out) <- "summary.stat_qap_cor"
  out
}


#' Print a summary of a QAP correlation result
#'
#' Print the object returned by \code{\link{summary.stat_qap_cor}}.
#'
#' @param x Object returned by \code{\link{summary.stat_qap_cor}}.
#' @param digits Number of significant digits to print.
#' @param ... Ignored.
#'
#' @return Invisibly returns \code{x}.
#' @export
print.summary.stat_qap_cor <- function(x, digits = 4, ...) {
  cat("\nSummary of QAP Correlation Test\n\n")
  cat("Correlation Method:", x$method, "\n")
  cat("Graph Type:", x$mode, "\n")
  cat("Diagonal Used:", x$diagonal, "\n")
  cat("Alternative:", x$alternative, "\n")
  cat("Controls:", x$controls.used, "\n")
  cat("Vertices:", x$n, "\n")
  cat("Permutations:", x$reps, "\n")
  cat("Valid replicate statistics:", x$valid.reps, "\n\n")
  cat("Observed Correlation:", format(signif(x$obs.stat, digits = digits)), "\n")
  cat("Pr(X>Obs):", format(signif(x$p.greater, digits = digits)), "\n")
  cat("Pr(X<Obs):", format(signif(x$p.less, digits = digits)), "\n")
  cat("Pr(X=Obs):", format(signif(x$p.equal, digits = digits)), "\n\n")
  cat("Null distribution summary:\n")
  print(signif(c(x$null.summary, `Std. Dev.` = x$null.sd), digits = digits))
  invisible(x)
}


