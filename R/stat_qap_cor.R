#' Plot a QAP correlation result
#'
#' Plot the permutation distribution from \code{\link{stat_qap_cor}} and mark
#' the observed correlation with a vertical line.
#'
#' @param x Object returned by \code{\link{stat_qap_cor}}.
#' @param main Plot title.
#' @param sub Plot subtitle.
#' @param breaks Histogram break specification.
#' @param col Fill color for the histogram bars.
#' @param border Border color for the histogram bars.
#' @param observed_col Color of the observed-value reference line.
#' @param observed_lwd Line width of the observed-value reference line.
#' @param xlab X-axis label.
#' @param prob Logical scalar; should a density histogram be drawn?
#' @param ... Additional graphical arguments passed to
#'   \code{\link[graphics]{hist}}.
#'
#' @return Invisibly returns \code{x}.
#' @export
plot.stat_qap_cor <- function(x,
                              main = "QAP Correlation Test",
                              sub = paste("Permutations:", x$reps, "Alternative:", x$alternative),
                              breaks = "Sturges",
                              col = "grey85",
                              border = "white",
                              observed_col = "firebrick",
                              observed_lwd = 2,
                              xlab = "Permutation correlations",
                              prob = TRUE,
                              ...) {
  rep_values <- x$rep.stat[!(is.na(x$rep.stat) | is.nan(x$rep.stat))]
  if (length(rep_values) == 0L) {
    stop("There are no valid replicate statistics to plot")
  }

  graphics::hist(
    rep_values,
    breaks = breaks,
    col = col,
    border = border,
    prob = prob,
    main = main,
    sub = sub,
    xlab = xlab,
    ...
  )
  if (!(is.na(x$obs.stat) | is.nan(x$obs.stat))) {
    graphics::abline(v = x$obs.stat, col = observed_col, lwd = observed_lwd)
  }
  invisible(x)
}


#' Convert a supported graph-like input to a QAP-ready matrix
#'
#' @keywords internal
#' @noRd
stat_qap_cor_prepare_matrix <- function(x,
                                        directed_override = NULL,
                                        reference = NULL) {
  if (!inherits(x, "igraph") &&
      !inherits(x, "network") &&
      !is.matrix(x) &&
      !is.data.frame(x)) {
    stop(
      "All QAP inputs should be 'igraph' objects, 'network' objects, square matrices, or edgelist data.frames"
    )
  }

  if (inherits(x, "data.frame")) {
    mat <- snafun::to_matrix(x, directed = directed_override)
  } else {
    mat <- snafun::to_matrix(x)
  }

  if (!is.matrix(mat) || nrow(mat) != ncol(mat)) {
    stop("'stat_qap_cor()' is only defined for one-mode square matrices")
  }
  if (anyNA(mat)) {
    stop("Missing values are currently not supported in 'stat_qap_cor()'")
  }

  storage.mode(mat) <- "numeric"
  mat <- stat_qap_cor_standardize_dimnames(mat = mat)

  if (!is.null(reference)) {
    mat <- stat_qap_cor_align_to_reference(mat = mat, reference = reference)
  }

  if (!is.null(directed_override) && !isTRUE(directed_override)) {
    if (!isSymmetric(unname(mat), check.attributes = FALSE)) {
      stop(
        "Undirected QAP correlations require symmetric matrices. ",
        "Please supply symmetric data or set 'directed = \"directed\"'."
      )
    }
  }

  mat
}


#' Standardize matrix dimnames for QAP work
#'
#' @keywords internal
#' @noRd
stat_qap_cor_standardize_dimnames <- function(mat) {
  row_names <- rownames(mat)
  col_names <- colnames(mat)

  if (is.null(row_names) && is.null(col_names)) {
    labels <- as.character(seq_len(nrow(mat)))
    rownames(mat) <- labels
    colnames(mat) <- labels
    return(mat)
  }

  if (is.null(row_names) && !is.null(col_names)) {
    rownames(mat) <- col_names
    row_names <- col_names
  }
  if (!is.null(row_names) && is.null(col_names)) {
    colnames(mat) <- row_names
    col_names <- row_names
  }

  if (!identical(sort(row_names), sort(col_names))) {
    stop("Row and column names of a QAP matrix should refer to the same vertices")
  }

  if (!identical(row_names, col_names)) {
    mat <- mat[row_names, row_names, drop = FALSE]
  }

  mat
}


#' Align a QAP matrix to a reference ordering
#'
#' @keywords internal
#' @noRd
stat_qap_cor_align_to_reference <- function(mat, reference) {
  ref_names <- rownames(reference)
  mat_names <- rownames(mat)

  if (is.null(ref_names) || is.null(mat_names)) {
    if (nrow(mat) != nrow(reference)) {
      stop("All QAP inputs should have the same number of vertices")
    }
    rownames(mat) <- rownames(reference)
    colnames(mat) <- colnames(reference)
    return(mat)
  }

  if (!setequal(ref_names, mat_names)) {
    stop("All QAP inputs should refer to the same vertex set")
  }

  mat[ref_names, ref_names, drop = FALSE]
}


#' Normalize control inputs for QAP correlation
#'
#' @keywords internal
#' @noRd
stat_qap_cor_prepare_controls <- function(controls, directed_flag, reference) {
  if (is.null(controls)) {
    return(list())
  }

  control_list <- controls
  if (inherits(control_list, "igraph") ||
      inherits(control_list, "network") ||
      is.matrix(control_list) ||
      is.data.frame(control_list)) {
    control_list <- list(control_list)
  }

  lapply(
    control_list,
    function(one_control) {
      stat_qap_cor_prepare_matrix(
        x = one_control,
        directed_override = directed_flag,
        reference = reference
      )
    }
  )
}


#' Resolve the directedness setting for QAP correlation
#'
#' @keywords internal
#' @noRd
stat_qap_cor_resolve_direction <- function(x, directed) {
  if (identical(directed, "auto")) {
    return(!isSymmetric(unname(x), check.attributes = FALSE))
  }
  identical(directed, "directed")
}


#' Vectorize a matrix for QAP correlation
#'
#' @keywords internal
#' @noRd
stat_qap_cor_vectorize <- function(x, diagonal, directed) {
  selector <- matrix(TRUE, nrow = nrow(x), ncol = ncol(x))
  if (!diagonal) {
    diag(selector) <- FALSE
  }

  as.numeric(x[selector])
}


#' Compute an observed or permuted QAP correlation
#'
#' @keywords internal
#' @noRd
stat_qap_cor_compute <- function(x, y, controls, method, diagonal, directed) {
  x_vec <- stat_qap_cor_vectorize(x = x, diagonal = diagonal, directed = directed)
  y_vec <- stat_qap_cor_vectorize(x = y, diagonal = diagonal, directed = directed)
  z_mat <- stat_qap_cor_vectorize_controls(
    controls = controls,
    diagonal = diagonal,
    directed = directed
  )

  vectors <- stat_qap_cor_transform_vectors(
    x = x_vec,
    y = y_vec,
    z = z_mat,
    method = method
  )

  if (ncol(vectors$z) == 0L) {
    if (stats::sd(vectors$x) == 0 || stats::sd(vectors$y) == 0) {
      return(NA_real_)
    }
    return(stats::cor(vectors$x, vectors$y, method = "pearson"))
  }

  x_resid <- stat_qap_cor_residualize(y = vectors$x, x = vectors$z)
  y_resid <- stat_qap_cor_residualize(y = vectors$y, x = vectors$z)
  if (stats::sd(x_resid) == 0 || stats::sd(y_resid) == 0) {
    return(NA_real_)
  }
  stats::cor(x_resid, y_resid, method = "pearson")
}


#' Vectorize control matrices for QAP correlation
#'
#' @keywords internal
#' @noRd
stat_qap_cor_vectorize_controls <- function(controls, diagonal, directed) {
  if (length(controls) == 0L) {
    return(matrix(numeric(0), nrow = length(logical(0)), ncol = 0L))
  }

  columns <- lapply(
    controls,
    function(one_control) {
      stat_qap_cor_vectorize(
        x = one_control,
        diagonal = diagonal,
        directed = directed
      )
    }
  )

  out <- do.call(cbind, columns)
  if (is.null(dim(out))) {
    out <- matrix(out, ncol = 1L)
  }
  out
}


#' Apply the selected correlation scale to QAP vectors
#'
#' @keywords internal
#' @noRd
stat_qap_cor_transform_vectors <- function(x, y, z, method) {
  if (identical(method, "spearman")) {
    x <- rank(x, ties.method = "average")
    y <- rank(y, ties.method = "average")
    if (ncol(z) > 0L) {
      z <- apply(z, 2, rank, ties.method = "average")
      if (is.null(dim(z))) {
        z <- matrix(z, ncol = 1L)
      }
    }
  }

  list(x = x, y = y, z = z)
}


#' Residualize a numeric vector on a control matrix
#'
#' @keywords internal
#' @noRd
stat_qap_cor_residualize <- function(y, x) {
  if (ncol(x) == 0L) {
    return(y)
  }

  design <- cbind("(Intercept)" = 1, x)
  fit <- stats::lm.fit(x = design, y = y)
  fit$residuals
}


#' Permute a matrix by relabeling vertices
#'
#' @keywords internal
#' @noRd
stat_qap_cor_permute_matrix <- function(x) {
  permutation <- sample.int(nrow(x))
  x[permutation, permutation, drop = FALSE]
}


#' Compute empirical p-values for a QAP permutation distribution
#'
#' @keywords internal
#' @noRd
stat_qap_cor_empirical_p <- function(obs_stat, rep_stats, alternative) {
  valid <- !(is.na(rep_stats) | is.nan(rep_stats))
  if (is.na(obs_stat) || is.nan(obs_stat) || !any(valid)) {
    return(list(
      p.value = NA_real_,
      p.greater = NA_real_,
      p.less = NA_real_,
      p.equal = NA_real_
    ))
  }

  rep_values <- rep_stats[valid]
  tie_tolerance <- sqrt(.Machine$double.eps)
  is_equal <- abs(rep_values - obs_stat) <= tie_tolerance
  p_equal <- mean(is_equal)
  p_greater <- mean(rep_values > (obs_stat + tie_tolerance))
  p_less <- mean(rep_values < (obs_stat - tie_tolerance))
  p_greater_or_equal <- p_greater + p_equal
  p_less_or_equal <- p_less + p_equal
  p_value <- switch(
    alternative,
    two.sided = min(1, 2 * min(p_greater_or_equal, p_less_or_equal)),
    greater = p_greater_or_equal,
    less = p_less_or_equal
  )

  list(
    p.value = p_value,
    p.greater = p_greater,
    p.less = p_less,
    p.equal = p_equal
  )
}


#' Temporarily set the RNG seed for a QAP test
#'
#' @keywords internal
#' @noRd
stat_qap_cor_set_seed <- function(seed) {
  if (is.null(seed)) {
    return(list(restore = NULL))
  }

  had_seed <- exists(".Random.seed", envir = .GlobalEnv, inherits = FALSE)
  old_seed <- if (had_seed) get(".Random.seed", envir = .GlobalEnv, inherits = FALSE) else NULL
  set.seed(as.integer(seed))

  restore <- function() {
    if (had_seed) {
      assign(".Random.seed", old_seed, envir = .GlobalEnv)
    } else if (exists(".Random.seed", envir = .GlobalEnv, inherits = FALSE)) {
      rm(".Random.seed", envir = .GlobalEnv)
    }
  }

  list(restore = restore)
}
